console.log("Example of while loop");
let n = Number.parseInt(prompt("Enter number : "));
let i = 0;
while (i < n) {
    console.log(i);
    i++;
}

console.log("Example of do while loop");
let n2 = Number.parseInt(prompt("Enter Another Number : "));
let j = 0;
do {
    console.log(j);
    j++;
} while (j < n2);